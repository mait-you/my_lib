#include "../include/libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (!lst || !del)
		return ;
	del(lst->content);
	ft_safe_allocate(NULL, FREE_ONE, lst, NULL);
}
