#include "../include/libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*current;

	if (!lst || !new)
		return ;
	if (!*lst)
		ft_lstadd_front(lst, new);
	else
	{
		current = ft_lstlast(*lst);
		current->next = new;
	}
}
